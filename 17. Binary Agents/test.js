const binaryAgent = require("./index");

test("binaryAgent function is defined.", () => {
  expect(binaryAgent).toBeDefined();
});

test("the argument passed to binaryAgent is a string.", () => {
  expect(binaryAgent(1)).toBe(undefined);
});

test("binaryAgent('01000001 01110010 01100101') returns a string.", () => {
  const result = binaryAgent("01000001 01110010 01100101");
  expect(typeof result).toBe("string");
});

test("binaryAgent('01000001 01110010 01100101 01101110 00100111 01110100 00100000 01100010 01101111 01101110 01100110 01101001 01110010 01100101 01110011 00100000 01100110 01110101 01101110 00100001 00111111') returns 'Aren't bonfires fun!?'", () => {
  expect(
    binaryAgent(
      "01000001 01110010 01100101 01101110 00100111 01110100 00100000 01100010 01101111 01101110 01100110 01101001 01110010 01100101 01110011 00100000 01100110 01110101 01101110 00100001 00111111"
    )
  ).toBe("Aren't bonfires fun!?");
});

test("binaryAgent('01001001 00100000 01101100 01101111 01110110 01100101 00100000 01000110 01110010 01100101 01100101 01000011 01101111 01100100 01100101 01000011 01100001 01101101 01110000 00100001') return 'I love FreeCodeCamp!'.", () => {
  expect(
    binaryAgent(
      "01001001 00100000 01101100 01101111 01110110 01100101 00100000 01000110 01110010 01100101 01100101 01000011 01101111 01100100 01100101 01000011 01100001 01101101 01110000 00100001"
    )
  ).toBe("I love FreeCodeCamp!");
});
